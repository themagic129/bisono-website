

$(function () {
    $(window).scroll(function () {
        if ($(window).scrollTop() > 20) {
            $('.navbar').addClass('cambio');

            
}

    else {
       
        $('.navbar').removeClass('cambio');
        
}
});
})


$(function () {
    $(window).scroll(function () {
        if ($(window).scrollTop() > 20) {
            $('.nav-link').addClass('nav-link-color-scroll');

            
}

    else {
       
        $('.nav-link').removeClass('nav-link-color-scroll');
        
}
});
})






